num = int(input('digite um número inteiro: '))      # recebendo um valor
if num % 2 == 0:                                    # se o resto da divisão do num por 2 for 0
    print(' o número {} é par!'.format(num))        # escreva que o número é par
else:                                               # senão
    print('o número {} é impar!'.format(num))       # escreva que o número é impar
print('-------Fim-------')                          # escreva fim
